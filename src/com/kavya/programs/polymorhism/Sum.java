package com.kavya.programs.polymorhism;

public class Sum
{
    public  int doSum(int number)
    {
        return number+10;
    }
    public int doSum(int number1, int number2)
    {
        return number1+number2;
    }

    public int doSum(int number1, int number2, int number3)
    {
        return number1+number2+number3;
    }
}
